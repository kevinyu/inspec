def to_xterm(r: int, g: int, b: int) -> int:
    """
    Convert a single RGB value to xterm-256color value
    """
    return int(np.argmin(np.linalg.norm([r, g, b] - _XTERM_COLORS_AS_RGB, axis=1)))
